public class N {
    private long p;
    private long q;

    public N(long p, long q) {
        this.p = p;
        this.q = q;
    }
    public long getP() {
        return p;
    }

    public void setP(long p) {
        this.p = p;
    }

    public long getQ() {
        return q;
    }

    public void setQ(long q) {
        this.q = q;
    }
}
